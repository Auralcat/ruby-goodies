html_file = open_html_source

# Put all found classes in here
html_classes = []
html_file.css('*').each do |element|
  buf = element.attr('class')
  unless buf.nil?
    if buf.include?(' ')
      buf.split(' ').each do |item|
        html_classes << item
      end
    else
      html_classes << buf
    end
  end
end
